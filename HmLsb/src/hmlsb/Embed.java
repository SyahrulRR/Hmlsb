    private void btnEmbedMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEmbedMouseClicked
        HuffmanModule hm = new HuffmanModule();
        LsbModule lsb = new LsbModule();
        
        String plaintext = readFile();        
        String character_plaintext = hm.getCharacter(plaintext);
        char character[] = character_plaintext.toCharArray();
        int frequency[] = new int[character_plaintext.length()];
        for(int i=0; i<character_plaintext.length(); i++){
            frequency[i] = hm.getFreqency(plaintext, character_plaintext.charAt(i));
        }
        
        hm.sortByFrequency(character, frequency);
        
        // create tree
        int n = character.length;
        PriorityQueue<HuffmanNode> q = new PriorityQueue<HuffmanNode>(n, new ImplementComparator());
        
        for(int i=0; i<n; i++){
            HuffmanNode hn = new HuffmanNode();
            
            hn.c = character[i];
            hn.item = frequency[i];
            
            hn.left = null;
            hn.right = null;
            
            q.add(hn);
        }
        
        HuffmanNode root = null;
        
        while(q.size() > 1){
            HuffmanNode x = q.peek();
            q.poll();
            
            HuffmanNode y = q.peek();
            q.poll();
            
            HuffmanNode f = new HuffmanNode();
            
            f.item = x.item + y.item;
            f.c = '-';
            f.left = x;
            f.right = y;
            root = f;
            
            q.add(f);
        }
        
        hm.printCode(root, "");
        
        // print output
        this.encodeMsg = hm.encode(hm.character, hm.code, plaintext);
        String nEncodeMsg = this.encodeMsg.length() + "";
        // System.out.println(plaintext + " = " + this.encode);
        
        // embed msg to image
        try {
            String pathOutput = "C:\\Users\\Syahrul\\Documents\\Hasil Data\\Embed\\Hasil Foto Stego\\stego.png";
            lsb.embed(this.pathCoverImage, pathOutput, this.encodeMsg);
            JOptionPane.showMessageDialog(this, "Stego Image berhasil disimpan di " + pathOutput);
        } catch (Exception e){
            System.out.println(e);
        }
        
        // write huffman code
        try {
            String pathOutput = "C:\\Users\\Syahrul\\Documents\\Hasil Data\\Embed\\HuffmanKey\\huffmancode.txt";
            FileWriter myWriter = new FileWriter(pathOutput);
            myWriter.write(hm.character);
            myWriter.write("\n");
            myWriter.write(hm.code);
            myWriter.write("\n");
            myWriter.write(nEncodeMsg);
            myWriter.close();
        } catch (IOException e){
            System.out.println(e);
        }
    }//GEN-LAST:event_btnEmbedMouseClicked

