    public String readFile(){
        String val = "";
        
        try {
            File myObj = new File(this.pathTxtFile);
            Scanner myReader = new Scanner(myObj);
            
            while(myReader.hasNextLine()){
                String data = myReader.nextLine();
                data = data.replace(" ", "-");
                val = val + data + ",";
            }
            
            myReader.close();
        } catch (FileNotFoundException e) {
            System.out.println(e);
        }
        
        return val;
    }

